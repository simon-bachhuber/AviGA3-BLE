/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{
  /* USER CODE BEGIN 1 */

  /* USER CODE END 1 */

  /* MCU Configuration--------------------------------------------------------*/

  /* Reset of all peripherals, Initializes the Flash interface and the Systick. */
  HAL_Init();
  /* Config code for STM32_WPAN (HSE Tuning must be done before system clock configuration) */
  MX_APPE_Config();

  /* USER CODE BEGIN Init */

  /* USER CODE END Init */

  /* Configure the system clock */
  SystemClock_Config();

/* Configure the peripherals common clocks */
  PeriphCommonClock_Config();

  /* IPCC initialisation */
  MX_IPCC_Init();

  /* USER CODE BEGIN SysInit */

  /* USER CODE END SysInit */

  /* Initialize all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_I2C1_Init();
  MX_SPI1_Init();
  MX_TIM1_Init();
  MX_ADC1_Init();
  MX_TIM2_Init();
  MX_RTC_Init();
  MX_TIM16_Init();
  MX_RF_Init();
  /* USER CODE BEGIN 2 */

	//MEAS: Sensor Init
	sensor = meas_initSensor(sensor, sensor_nr);

	//IMU: Initialize BMI270
	imu_init(&bmi2_dev);

	//I2C: Set I2C-Slave Address
	hi2c1.Init.OwnAddress1 = sensor.i2c_address;
	HAL_I2C_Init(&hi2c1);

	//I2C: Start listening for commands
	HAL_I2C_Slave_Receive_IT(&hi2c1, i2c_buffer, I2C_HEADER_SIZE);

  /* USER CODE END 2 */

  /* Init code for STM32_WPAN */
  MX_APPE_Init();

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
	while (1)
	{
		//COMMON: Switch LED
		common_Handler();

		//MEAS: Start/Stop measurement, Process new FIFO data
		meas_Handler();

		//POWER: Get status of charger pin and control green led
		powerManagement_getChargerState();

		//POWER: Get capacity of battery in %
		battery_level = powerManagement_getBatteryLevel();

		//SLEEP mode for energy saving in BLE
		if(sensor.active == ON && sensor.transfer_mode == MODE_BLE)
		{
			HAL_SuspendTick();
			HAL_PWR_EnterSLEEPMode(PWR_MAINREGULATOR_ON, PWR_SLEEPENTRY_WFI);
		}

    /* USER CODE END WHILE */
    MX_APPE_Process();

    /* USER CODE BEGIN 3 */
	}
  /* USER CODE END 3 */
}

